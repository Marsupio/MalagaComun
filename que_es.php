<h2 align='left'>¿Cómo funciona?</h2>
                                        
<p align="justify">Todas las transacciones pasan por un sistema informático que alberga las cuentas de cada usuario. Es transparente de manera que todos los miembros pueden ver las cuentas y transacciones de los demás. El Común se valora a través del tiempo de trabajo, de forma que una hora de trabajo (independientemente de su naturaleza) equivalga a 10 comunes. También se suele hacer una correspondencia con el euro, para facilitar la valoración de los objetos. Pero los criterios que usamos no son los que impone el mercado, sino la justicia, la ética y la solidaridad.</p>                            
                                   
<p align="justify">Pero al final estamos hablando del dinero, ¿no? Sí, pero este dinero (moneda complementaria) está creada por la comunidad según sus necesidades , y entonces nunca es escasa como en el caso de las monedas convencionales. Se queda dentro de la comunidad y está basada en valores reales, no especulativos, sin intereses. Cuando una persona hace cualquier intercambio está ayudando a la comunidad en vez de siempre buscar lo más barato y pisar a los demás para salir ganando. Lo bonito del sistema reside en que siempre el balance está a cero. Siempre habrá gente con crédito y con débito.</p>
                          
<p align="justify">Al empezar en el sistema, estamos a cero. Por ejemplo, yo hago un trabajo por ti y me pagas 10 comunes, entonces yo tengo +10 y tu -10. Hay que cambiar el chip de pensar que es negativo o malo tener débito. En este sistema el débito (deudas) significa que has contribuido a la comunidad, y estás comprometido a participar en el futuro. Para dar una orientación solemos decir que una hora de trabajo son unos 10 comunes, pero es flexible, por supuesto.</p>

