<h2 align='left'>¿Qué es un sistema de intercambio?</h2>
<img src="images/intercambio.png" style='float:right' style='padding-left:20px' alt=""  width="40%"/>
   
<p align="justify">Un sistema de intercambio puede incluir sistemas como el Trueque, el Banco del Tiempo (BdT), o Moneda Complementaria. En todos se trata del intercambio de bienes o servicios. En un BdT la moneda virtual de intercambio es el tiempo medido en horas. Una persona ofrece un servicio, recibiendo una hora de crédito o más, según el tiempo dedicado. Esta persona puede gastar el crédito acumulado en recibir servicios de otra persona. Los BdT suelen estar involucrados en trabajos sociales y dentro de una cierta comunidad, reforzando los enlaces entre la gente. El trueque es distinto: se hace un intercambio directamente entre las personas. Un bien por otro. Estos dos sistemas funcionan muy bien y ya hay muchos en todo el mundo. Sin embargo el sistema de Monedas Complementarias (LETS) es más flexible ya que no se restringe al intercambio de servicios.</p>

</div>
