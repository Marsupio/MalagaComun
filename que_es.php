<p align='justify'>El dinero es una herramienta artificial para facilitar el intercambio de bienes y servicios. Mucha gente cree que sin dinero no se pueden intercambiar bienes y servicios, pero eso es falso. El problema de las “crisis” es que no se mueve mucho dinero, y se pierden empleos. Eso no significa que la gente sin trabajo no tenga nada que ofrecer a la sociedad, sino que no hay dinero para pagarle sus servicios. Desde tiempo inmemorial la gente ha sabido intercambiar bienes y servicios sin mediar dinero. En nuestra era, Internet nos facilita la tarea de conseguir bienes y servicios usando nuestra propia moneda, y ofreciendo nuestro trabajo, nuestras habilidades, nuestra compañía, y cualquier objeto que ya no necesitemos. En Málaga contamos con una moneda social llamada <i>común</i>. En España y el resto del mundo están funcionando cada vez más sistemas de monedas complementarias.  </p>
